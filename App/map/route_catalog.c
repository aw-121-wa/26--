static int8_t treasure_offset(uint8_t node)
{
    switch (node)
    {
    case P1: return 0;
    case P3: return 1;
    case P4: return 2;
    case P5: return 3;
    case P6: return 4;
    default: return -1;
    }
}

const uint8_t *RouteCatalog_GetDoor(uint8_t route_number)
{
    if (route_number == 0u || route_number > ROUTE_CATALOG_DOOR_COUNT)
        return 0;
    return door_routes[route_number - 1u];
}

const uint8_t *RouteCatalog_GetReturn(uint8_t route_number)
{
    if (route_number == 0u || route_number > ROUTE_CATALOG_RETURN_COUNT)
        return 0;
    return return_routes[route_number - 1u];
}

const uint8_t *RouteCatalog_SelectReturn(const MissionRouteInput_t *input)
{
    uint8_t group;
    uint8_t route_number;
    int8_t treasure;

    if (input == 0 || input->green_gate < 1u || input->green_gate > 4u)
        return 0;

    if (input->clue_a == 5u && input->clue_b == 7u) group = 0u;
    else if (input->clue_a == 5u && input->clue_b == 8u) group = 1u;
    else if (input->clue_a == 6u && input->clue_b == 7u) group = 2u;
    else if (input->clue_a == 6u && input->clue_b == 8u) group = 3u;
    else return 0;

    treasure = treasure_offset(input->treasure_node);
    if (treasure < 0)
        return 0;

    route_number = (uint8_t)(group * 20u + (4u - input->green_gate) * 5u +
                             (uint8_t)treasure + 1u);
    return RouteCatalog_GetReturn(route_number);
}

RouteBuildStatus_t RouteCatalog_AppendDoor(RouteBuilder_t *builder,
                                            uint8_t route_number)
{
    const uint8_t *segment = RouteCatalog_GetDoor(route_number);
    return segment == 0 ? ROUTE_BUILD_INVALID_ARG :
                          RouteBuilder_AppendSegment(builder, segment);
}

RouteBuildStatus_t RouteCatalog_AppendReturn(RouteBuilder_t *builder,
                                              const MissionRouteInput_t *input)
{
    const uint8_t *segment = RouteCatalog_SelectReturn(input);
    return segment == 0 ? ROUTE_BUILD_INVALID_ARG :
                          RouteBuilder_AppendSegment(builder, segment);
}

uint8_t RouteCatalog_ValidateAll(void)
{
    uint8_t i;
    uint8_t j;

    for (i = 0u; i < ROUTE_CATALOG_DOOR_COUNT; i++)
    {
        for (j = 0u; j < ROUTE_CAPACITY && door_routes[i][j] != ROUTE_END; j++)
        {
            if (door_routes[i][j] >= MAP_NODE_COUNT) return 0u;
            if (j > 0u && getNextConnectNode(door_routes[i][j - 1u],
                                             door_routes[i][j]) == MAP_NODE_INDEX_INVALID)
                return 0u;
        }
        if (j == ROUTE_CAPACITY) return 0u;
    }
    for (i = 0u; i < ROUTE_CATALOG_RETURN_COUNT; i++)
    {
        for (j = 0u; j < ROUTE_CAPACITY && return_routes[i][j] != ROUTE_END; j++)
        {
            if (return_routes[i][j] >= MAP_NODE_COUNT) return 0u;
            if (j > 0u && getNextConnectNode(return_routes[i][j - 1u],
                                             return_routes[i][j]) == MAP_NODE_INDEX_INVALID)
                return 0u;
        }
        if (j == ROUTE_CAPACITY) return 0u;
    }
    return 1u;
}
